package com.ossobo.winterfx.notifications.position;

/**
 * ScreenPosition v1.0
 * Define onde a notificação aparece na tela.
 */
public enum ScreenPosition {
    CENTER,
    TOP_LEFT,
    TOP_CENTER,
    TOP_RIGHT,
    CENTER_LEFT,
    CENTER_RIGHT,
    BOTTOM_LEFT,
    BOTTOM_CENTER,
    BOTTOM_RIGHT,
    OWNER_CENTER,      // Centro do owner
    OWNER_TOP,         // Topo do owner
    OWNER_BOTTOM,      // Base do owner
    MOUSE_POSITION     // Onde o mouse está
}
